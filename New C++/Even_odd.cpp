#include <iostream>
using namespace std;
int main(){
	
	
	int num;
	cout<<"Enter The Number";
	cin>>num;
	if(num%2==0){
		cout<<num<<" Is an Even-Number";
	}else
		if(num%2!=0){
			cout<<num<<" IS an Odd-Number";
		}
		else{
			cout<<"ERROR";
		
	}
	
	
	
	
	
	
	return 0;
}
